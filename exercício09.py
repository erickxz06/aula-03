numero = int(input("Número: "))

if numero <1  or numero >12:
    print(f"Número inválido")
else:
    if numero == 1:
        print(f"Janeiro")
    elif numero == 2:
        print(f"Fevereiro")
    if numero == 3:
        print(f"Março")
    elif numero == 4:
        print(f"Abril")
    if numero == 5:
        print(f"Maio")
    elif numero == 6:
        print(f"Junho")
    if numero == 7:
        print(f"Julho")
    elif numero == 8:
        print(f"Agosto")
    if numero == 9:
        print(f"Setembro")
    elif numero == 10:
        print(f"Outubro")
    if numero == 11:
        print(f"Novembro")
    elif numero == 12:
        print(f"Dezembro")

